package Lab_03;
public class PrimTypes {
    public static void main(String[] args) {
        boolean binary = true;
        byte bitten = 's';
        char mainCharacter = 'A';
        short small = 32;
        int mid = 64;
        long large = 128;
        float num = 12;
        double numb = 12.1;

        System.out.println("boolean: " + binary);
        System.out.println("byte: " + bitten);
        System.out.println("char: " + mainCharacter);
        System.out.println("short: " + small);
        System.out.println("int: " + mid);
        System.out.println("long: " + large);
        System.out.println("float: " + num);
        System.out.println("double: " + numb);

    }
}
